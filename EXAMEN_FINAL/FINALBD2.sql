CREATE OR REPLACE FUNCTION EXTRAER_PRECIO_PRODUCTO(ID_PRODUCTO NUMBER)
RETURN NUMBER
IS
PRECIO NUMBER(12,2);
BEGIN
    SELECT PRECIO_VENTA INTO PRECIO FROM PRODUCTO WHERE COD_PRODUCTO=ID_PRODUCTO;
    RETURN PRECIO;
    EXCEPTION
    WHEN NO_DATA_FOUND THEN
    DBMS_OUTPUT.PUT_LINE('NO EXISTE UN PRODUCTO CON ESE CODIGO, VERIFIQUE POR FAVOR');
END EXTRAER_PRECIO_PRODUCTO;



/*FUNCION PARA EXTRAER EL MONTO TOTAL DE LOS ARTICULOS POR CODIGO DE PRODUCTO EN UNA VENTA*/
CREATE OR REPLACE FUNCTION MONTO_TOTAL(COD_PRODUCTO NUMBER,CANTIDAD NUMBER)
RETURN NUMBER
IS 
MONTO_PARCIAL NUMBER(12,2);
MONTO_TOTAL NUMBER(12,2);
BEGIN
    SELECT EXTRAER_PRECIO_PRODUCTO(COD_PRODUCTO)INTO MONTO_PARCIAL FROM DUAL;
    MONTO_TOTAL:=CANTIDAD*MONTO_PARCIAL;
    RETURN MONTO_TOTAL;
END;



