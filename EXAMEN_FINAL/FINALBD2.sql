compelto o anulado*/

CREATE OR REPLACE TRIGGER BI_DETALLE_PEDIDO
BEFORE INSERT ON DETALLE_PEDIDO 
FOR EACH ROW
DECLARE
/*PARA SABER EL ESTATUS DEL PEDIDO*/
ESTADO_VENTA NUMBER(3);
/*VALIDAR QUE EXISTE EL PRODUCTO*/
EXISTE_PROD NUMBER(9);
/*VALIDAMOS LA EXISTENCIA DEL PRODUCTO*/
HAY_EXISTENCIA NUMBER(9);
BEGIN
    /*PRIMERO VALIDAMOS SI EL CÓDIGO DEL PRODUCTO ES EL CORRECTO, SINO REBOTAMOS TODA LA ORDEN*/
    SELECT EXISTE_PRODUCTO(:NEW.COD_PRODUCTO)INTO EXISTE_PROD FROM DUAL;
    IF(EXISTE_PROD>0)THEN
        SELECT ESTATUS_PEDIDO(:NEW.COD_PEDIDO)INTO ESTADO_VENTA FROM DUAL;
        /*SI EL ESTADO DE LA VENTA COMPLETADA O ANULADA DEBE RESTAR O SUMAR LA EXISTENCIA
        RESPECTIVAMENTE*/
        /*SI LA VENTA ESTÁ COMPLETADA, RESTAR EXISTENCIA*/
        IF(ESTADO_VENTA=2)THEN
            /*VALIDAMOS QUE HAYA EXISTENCIA Y ADEMAS    QUE SEA MAYOR A LA CANTIDAD SOLICITADA, NO PUEDE SER MENOR A 0*/
            SELECT EXTRAER_EXISTENCIA_PRODUCTO(:NEW.COD_PRODUCTO)INTO HAY_EXISTENCIA FROM DUAL;
            IF(HAY_EXISTENCIA>0 AND HAY_EXISTENCIA>=:NEW.CANTIDAD_PROD) THEN
            
                UPDATE PRODUCTO SET EXISTENCIA=EXISTENCIA-:NEW.CANTIDAD_PROD WHERE COD_PRODUCTO=:NEW.COD_PRODUCTO;
            ELSE
                RAISE_APPLICATION_ERROR(-20003,'NO HAY EXISTENCIA DEL PRODUCTO '||:NEW.COD_PRODUCTO||
                ' O ES INSUFICIENTE PARA SATISFACER EL PEDIDO REVISE POR FAVOR');
            END IF;
        /*SI LA VENTA SE ANULA, DEVOLVEMOS LAS EXISTENCIAS AL PRODUCTO*/
        ELSIF(ESTADO_VENTA=4)THEN
            UPDATE PRODUCTO SET EXISTENCIA=EXISTENCIA+:NEW.CANTIDAD_PROD WHERE COD_PRODUCTO=:NEW.COD_PRODUCTO;
        /*SI ESTÁ EN PROCESO O CANCELADA NO OPERAMOS NADA*/
        ELSIF(ESTADO_VENTA=1 OR ESTADO_VENTA=3)THEN
            DBMS_OUTPUT.PUT_LINE('');
        ELSE
        /*NO EXISTE EL NUMERO DE TIPO DE PEDIDO*/
            RAISE_APPLICATION_ERROR(-20002,'NO EXISTE TIPO DE PEDIDO CON EL ID '||ESTADO_VENTA||'    REVISE POR FAVOR');
        END IF;
    ELSE
        RAISE_APPLICATION_ERROR(-20001,'NO EXISTE UN PRODUCTO CON EL ID '||:NEW.COD_PRODUCTO||'    REVISE POR FAVOR');
    END IF;

END AI_DETALLE_PEDIDO;



